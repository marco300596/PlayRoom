package logic.bean;

public class RegistrationBean {

		private String firstNameReg;
		private String lastNameReg;
		private String userNameReg;
		private String emailReg;
		private String passwordReg;

		public RegistrationBean() {
			//this is empty
		}

		public String getFirstNameReg() {
			return firstNameReg;
		}

		public String getLastNameReg() {
			return lastNameReg;
		}

		public String getUserNameReg() {
			return userNameReg;
		}

		public String getEmailReg() {
			return emailReg;
		}

		public String getPasswordReg() {
			return passwordReg;
		}

		public void setFirstNameReg(String firstNameReg) {
			this.firstNameReg = firstNameReg;
		}

		public void setLastNameReg(String lastNameReg) {
			this.lastNameReg = lastNameReg;
		}

		public void setUserNameReg(String userNameReg) {
			this.userNameReg = userNameReg;
		}

		public void setEmailReg(String emailReg) {
			this.emailReg = emailReg;
		}

		public void setPasswordReg(String passwordReg) {
			this.passwordReg = passwordReg;
		}

}